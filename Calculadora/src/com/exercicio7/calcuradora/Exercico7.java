    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        int num = Integer.parseInt(txtNum.getValue().toString());
        //Resto
        int res = num % 2;
        lblResto.setText(Integer.toString(res));
        //Cubo
        double cub = Math.pow(num,3);
        lblCubo.setText(Double.toString(cub));
        //Raiz Quadrada
        double qd = Math.sqrt(num);
        lblRaizQ.setText(String.format("%.2f",qd));
        
        // Raiz Cubica
        double cb = Math.cbrt(num);
        lblRaizC.setText(String.format("%.3f", cb));
        
        int abs = Math.abs(num);
        lblAbsolut.setText(Integer.toString(abs));
        
        panCalc.setVisible(true);
    }//GEN-LAST:event_btnCalcActionPerformed

